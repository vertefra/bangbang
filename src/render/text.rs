pub const GLYPH_W: i32 = 5;
pub const GLYPH_STEP: i32 = 6;

pub const FONT_ATLAS_CELL: u32 = 8;
pub const FONT_ATLAS_COLS: u32 = 16;
pub const FONT_ATLAS_ROWS: u32 = 6;

pub fn build_font_atlas_rgba() -> (Vec<u8>, u32, u32) {
    let w = FONT_ATLAS_COLS * FONT_ATLAS_CELL;
    let h = FONT_ATLAS_ROWS * FONT_ATLAS_CELL;
    let mut rgba = vec![0u8; (w * h * 4) as usize];
    for c in 32u8..127u8 {
        let i = (c - 32) as usize;
        let col = (i % 16) as u32;
        let row = (i / 16) as u32;
        let ox = col * FONT_ATLAS_CELL + 1;
        let oy = row * FONT_ATLAS_CELL + 1;
        let g = glyph(c);
        for gy in 0..7usize {
            for gx in 0..5usize {
                if (g[gy] >> (4 - gx)) & 1 != 0 {
                    let px = ox + gx as u32;
                    let py = oy + gy as u32;
                    let idx = ((py * w + px) * 4) as usize;
                    rgba[idx..idx + 4].copy_from_slice(&[255, 255, 255, 255]);
                }
            }
        }
    }
    (rgba, w, h)
}

